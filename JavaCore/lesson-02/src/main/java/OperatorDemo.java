public class OperatorDemo {
    public static void main(String[] args) {
        // Toán tử số học
        // + - * /
        int a = 5;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        // += -= *= /=
        a += 10;
        System.out.println("a = " + a);
        int c = a++;
        //int c = a;
        //int a = a + 1
        int d = ++a;
        // a = a + 1
        // int d = a

        // int + int = int
        // int + long = long
        //long + float = float
        // byte -> short -> int -> long -> float -> double

        // Toán tử quan hệ

    }
}
